      {
        fristname : firstName.value,
        lastname  : lastName.value,
        useremail : userEmail.value,
        userpassword : userPassword.value,
        repassword : rePassword.value
      }
      newArray.push(users);

      localStorage.setItem("userData",JSON.stringify(newArray));
